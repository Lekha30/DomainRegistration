package com.lekha.companyA.bean;

public class DomainBean {
	private String domainName;
	private double domainPrice;

	/**
	 * @return the domainName
	 */
	public String getDomainName() {
		return domainName;
	}

	/**
	 * @param domainName
	 *            the domainName to set
	 */
	public void setDomainName(String domainName) {
		this.domainName = domainName;
	}

	/**
	 * @return the domainPrice
	 */
	public double getDomainPrice() {
		return domainPrice;
	}

	/**
	 * @param domainPrice
	 *            the domainPrice to set
	 */
	public void setDomainPrice(double domainPrice) {
		this.domainPrice = domainPrice;
	}

}
